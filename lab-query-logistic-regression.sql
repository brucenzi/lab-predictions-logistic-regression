USE SAKILA;

SELECT * FROM RENTAL ORDER BY RENTAL_DATE DESC;
SELECT * FROM INVENTORY;
SELECT * FROM FILM;

SELECT 
	R.RENTAL_ID, DATE_FORMAT(R.RENTAL_DATE, '%Y-%M') AS RENTAL_DATE_NEW,  
	F.RENTAL_DURATION, R.CUSTOMER_ID, I.STORE_ID, R.INVENTORY_ID, F.FILM_ID, F.LENGTH
FROM RENTAL R
LEFT JOIN INVENTORY I ON R.INVENTORY_ID = I.INVENTORY_ID
LEFT JOIN FILM F ON I.FILM_ID = F.FILM_ID;


